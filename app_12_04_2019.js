const n1 = 100;
const n2 = 50;
let val;

val = n1 + n2;
// console.log(val); --> 150

val = n1 - n2;
// console.log(val); --> 50

val = n1 * n2;
// console.log(val); --> 5000

val  = n1 / n2;
// console.log(val); --> 2

val = n1 % n2; // remainder
// console.log(val); --> 0


//=============================================

// Math object - has properties/attributes and methods/functions

val = Math.PI;
// console.log(val); --> 3.14....


//round to NEAREST WHOLE:
val = Math.round(2.6);
// console.log(val); --> 3
// nearest whole

val = Math.round(2.3);
// console.log(val); --> 2
// nearest whole


// round UP and DOWN:

val = Math.ceil(2.3);
// console.log(val); --> 3
// rounds up


val = Math.floor(2.3);
// console.log(val); --> 2
// round down


val = Math.sqrt(64);
// square root
// console.log(val); --> 8

val = Math.pow(8,2);
// powers
// console.log(val); --> 64



